export function detectBrowserLang() {
  if (typeof navigator === 'undefined') return 'fr';
  const raw = (navigator.language || navigator.userLanguage || 'fr').toLowerCase();
  if (raw.startsWith('ar')) return 'ar';
  if (raw.startsWith('en')) return 'en';
  return 'fr';
}
